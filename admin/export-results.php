<?php

require_once "../config/config.php";
require_once "../config/database.php";
require_once "../includes/session.php";
require_once "../vendor/autoload.php";

use Dompdf\Dompdf;
use Dompdf\Options;

if (!isset($_SESSION['admin_id'])) {
    header("Location: login.php");
    exit();
}

$totalStudents = $pdo->query("
SELECT COUNT(*)
FROM students
")->fetchColumn();

$totalCandidates = $pdo->query("
SELECT COUNT(*)
FROM candidates
")->fetchColumn();

$totalVotes = $pdo->query("
SELECT COUNT(*)
FROM votes
")->fetchColumn();

$totalVoted = $pdo->query("
SELECT COUNT(*)
FROM students
WHERE has_voted=1
")->fetchColumn();

$turnout = 0;

if($totalStudents > 0){
    $turnout = round(($totalVoted/$totalStudents)*100,2);
}
$stmt = $pdo->query("

SELECT

c.fullname,

COUNT(v.id) AS total_votes

FROM candidates c

LEFT JOIN votes v
ON c.id=v.candidate_id

GROUP BY c.id,c.fullname

ORDER BY total_votes DESC,
fullname ASC

LIMIT 10

");

$leaders = $stmt->fetchAll();

$date = date("F d, Y h:i A");

$logo = realpath("../assets/images/ssite-logo.png");

$html = '

<!DOCTYPE html>

<html>

<head>

<style>

body{
    font-family: DejaVu Sans, sans-serif;
    font-size:13px;
    color:#333;
}

.header{
    text-align:center;
    margin-bottom:20px;
}

.logo{
    width:90px;
    margin-bottom:10px;
}

.title{
    font-size:24px;
    color:#001F54;
    font-weight:bold;
}

.subtitle{
    font-size:15px;
    color:#555;
}

table{
    width:100%;
    border-collapse:collapse;
    margin-top:20px;
}

th{
    background:#001F54;
    color:white;
    border:1px solid #ccc;
    padding:10px;
}

td{
    border:1px solid #ccc;
    padding:8px;
}

.footer{
    margin-top:30px;
    text-align:center;
    font-size:12px;
    color:#777;
}

</style>
</head>
<body>

<div class="header">

<div class="title">
SSITE Elections Portal
</div>

<div class="subtitle">
Student Society of Information Technology Education
</div>
<h2>Election Results Report</h2>
</div>

<p><strong>Date Generated:</strong> '.$date.'</p>

<table>
<tr>
<th colspan="2">
Election Summary
</th>
</tr>

<tr>
<td>Registered Students</td>
<td>'.$totalStudents.'</td>
</tr>

<tr>
<td>Total Candidates</td>
<td>'.$totalCandidates.'</td>
</tr>
<tr>

<td>Votes Cast</td>
<td>'.$totalVotes.'</td>
</tr>

<tr>
<td>Voter Turnout</td>
<td>'.$turnout.'%</td>
</tr>

</table>
<br>

<table>
<tr>
<th>Rank</th>
<th>Candidate</th>
<th>Votes</th>
</tr>
';

$rank = 1;

foreach($leaders as $candidate){

    if($rank == 1){

        $position = "1st";

    }elseif($rank == 2){

        $position = "2nd";

    }elseif($rank == 3){

        $position = "3rd";

    }else{

        $position = $rank;

    }

    $html .= '

    <tr>

    <td>'.$position.'</td>

    <td>'.htmlspecialchars($candidate['fullname']).'</td>

    <td>'.$candidate['total_votes'].'</td>

    </tr>

    ';

    $rank++;

}

$html .= '

</table>

<div class="footer">

<hr>

Generated by

<strong>'.htmlspecialchars($_SESSION['admin_name']).'</strong>

<br>

'.$date.'

</div>

</body>

</html>

';

$options = new Options();

$options->set("isRemoteEnabled", true);

$dompdf = new Dompdf($options);

$dompdf->loadHtml($html);

$dompdf->setPaper("A4","portrait");

$dompdf->render();

$dompdf->stream("Election_Results.pdf",["Attachment"=>true]);

exit();